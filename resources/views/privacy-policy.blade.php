# Privacy Policy – MGRH Glasgow

Effective Date: May 2026

MGM Glasgow (“we”, “our”, or “us”) operates the MGM Glasgow staff management and hotel operations application.

This Privacy Policy explains how we collect, use, and protect information when employees and authorised users use our application and related services.

## 1. Information We Collect

We may collect the following information:

* Name
* Email address
* Role and department information
* Attendance and rota records
* GPS location during attendance clock-in/out
* Device and login information
* Inventory and operational activity logs

## 2. How We Use Information

We use collected information to:

* Manage hotel staff operations
* Record attendance and shift schedules
* Improve internal hotel management
* Maintain system security
* Generate operational reports
* Provide support and notifications

## 3. GPS & Location Data

The application may collect GPS location data when staff clock in or clock out. This is used solely for attendance verification and fraud prevention.

We do not track users continuously in the background.

## 4. Data Security

We implement industry-standard security measures to protect stored information, including:

* Encrypted connections (HTTPS)
* Secure authentication
* Restricted role-based access
* Protected server infrastructure

## 5. Data Sharing

We do not sell or share personal information with third parties except when required by law or necessary for technical infrastructure and service delivery.

## 6. User Access

Only authorised hotel management and system administrators may access operational data based on role permissions.

## 7. Changes to This Policy

We may update this Privacy Policy periodically. Updates will be published on this page.

